package jenny;

public class No2 {

    public static void main(String[] args) {

        double suhuCelcius, suhuReamur, suhuFahrenheit;

        suhuCelcius = 3;
        suhuReamur = (4.0 / 5.0) * suhuCelcius;
        suhuFahrenheit = (9.0 / 5.0) * suhuCelcius + 32;

        System.out.println("Suhu dalam celcius = " + suhuCelcius + " Dejarat");
        System.out.println("Suhu ini setara dengan = " + suhuReamur + " Derajat reamur");
        System.out.println("Suhu ini setara dengan = " + suhuFahrenheit + " Derajat Fahrenheit");

    }

}
